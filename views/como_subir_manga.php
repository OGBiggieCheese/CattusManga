<h1> <b><center>¿Cómo subir tu manga? </center></b></h1>
<br>

<h3>  <b> Ingrese el título del manga  </b>   </h3>
<p> <h5>  En este recuadro es donde se tendrá que colocar el nombre del manga, preferiblemente el nombre debe ser el original del japonés.  </h5></p>
<br>
<h3> <b>  Ingrese la sinopsis del manga     </b> </h3>
<p> <h5>  En este recuadro es donde se tendrá que colocar la sipnosis o de que se va a tratar el manga. </h5></p>
<br>
<h3> <b>     Elija las categorías del manga   </b> </h3>
<p> <h5>  En este recuadro es donde se tendrá que colocar las categorías del manga, en el cual se podrá elegir entre 1 o más categorías para tu manga, puede elegir entre las categoraías más famosas como por ejemplo shounens, romances, comedias y más.</h5></p>
<br> 
<br>
<h3> <b>      Elija que usuarios lo pueden ver  </b> </h3>
<p> <h5>  En este recuadro es donde se tendrá que colocar que tipos de usuario podrá ver su manga, tiene que elegir entre, usuario normal (sin suscripción), Cat (suscripción nivel 1) y Cattus (suscrpición nivel 2). Puede elegir entre uno o más opciones.  </h5></p>
<br>

<h3> <b>  Ingresar los archivos del manga     </b> </h3>
<p> <h5>  En este recuadro es donde se tendrá que colocar sus mangas, usted debe subir 2 partes en el cual son: portada y capítulos.
<br>    
<br> 
<b>Importante leer: </b>
</br>
<br>
ඞ Portada: Para subir la portada, el archivo debe tener el nombre de "portada" y su formato debe ser .png, sino el archivo no se subirá correctamente, además el tamaño de la portada debe ser de 1012x1600px. 
</br>
<br>
ඞ Capítulos: Para subir los capítulos del manga, los archivos deben tener de nombre números consecutivos, comenzando con el numero 1, por ejemplo, la primera parte tendrá de nombre "1", la parte 2 tendrá de nombre "2" y la parte 10 tendrá de nombre "10" y el formato de cada capitulo debe ser en .jpg sino los capítulos no se subirán correctamente. Los capítulos deben tener la traducción al español y pueden ser blaco y negro o en color. 
<br>
<br>
ඞ Paginas: Para subir las paginas del capitulo, los archivos deben tener de nombre números consecutivos, comenzando con el numero 1, por ejemplo, la primera pagina tendrá de nombre "1", la pagina 2 tendrá de nombre "2" y la pagina 10 tendrá de nombre "10" y el formato de cada capitulo debe ser en .jpg sino las paginas no se subirán correctamente.
<br>
<br>
ඞ Todos los mangas pasaran por una fase de verificacion antes de ser lanzados al publico. 
</h5></p>


